#include "3-calc.h"

/**
 * op_add - calcs sum of a and b
 * @a: first member
 * @b: second member
 *
 * Return: a + b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - difference of a and b
 * @a: first member
 * @b: second member
 *
 * Return: a - b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplication of a and b
 * @a: first member
 * @b: second member
 *
 * Return: a * b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of a and b
 * @a: first member
 * @b: second member
 *
 * Return: a / b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod  - remainder of a and b
 * @a: first member
 * @b: second member
 *
 * Return: a % b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
